class Integer {
	public static void main(String args[]) {
		byte b = 10;
		System.out.println("value of b: " + b);
		b *= 10;
		System.out.println("value of b: " + b);
		b += 100;
		System.out.println("value of b: " + b);
		byte c = -128;
		System.out.println("value of c: " + c);
		c -= 1;
		System.out.println("value of c: " + c);
	}
}