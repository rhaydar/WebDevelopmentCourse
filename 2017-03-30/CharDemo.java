class CharDemo {
	public static void main(String args[]) {
		char ch = 'A';
		System.out.println("value of ch: " + ch);
		ch += 2;
		System.out.println("value of ch: " + ch);
		ch = 90;
		System.out.println("value of ch: " + ch);
		ch++;
		System.out.println("value of ch: " + ch);
		ch = 128;
		System.out.println("value of ch: " + ch);
	}
}