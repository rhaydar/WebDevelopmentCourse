class Example3 {
	public static void main(String args[]) {
		int y;
		double x;

		y = 10;
		x = 10.0;

		System.out.println("Original value of y: " + y);
		System.out.println("Original value of x: " + x);
		System.out.println();

		y = y/4;
		x = x/4;

		System.out.println("y after division: " + y);
		System.out.println("x after division: " + x);
	}
}